void main() {
  var day = 2;
  switch (day) {
    case 1:
      print("Monday");
      break;
    case 2:
      print("Tuesday");
      break;
    case 3:
      print("Wednesday");
      break;
    case 4:
      print("Friday");
      break;
    case 5:
      print("Saturday");
      break;
    case 6:
      print("Sunday");
      break;
    default:
      print("invalid");
      break;
  }
}
