void main() {
  var x = 22;
  var y = 39;
  print('logical operator');
  if (x < y && x > y) {
    print("true");
  } else {
    print("false");
  }
  if (x < y || x > y) {
    print("true");
  } else {
    print("false");
  }
  if (x != y || x == y) {
    print("true");
  } else {
    print("false");
  }
}
